                <h4>Sportgelände und Vereinsheim</h4>
                <div class="text mb-3">
                    1951 wurde der erste Sportplatz auf dem Gelände des heutigen Kunstrasenplatzes eingeweiht. Umkleideräume wurden gebaut, 1955 durch Umkleideräume mit Waschgelegenheit erweitert. Der Spielbetrieb weitete sich aus, auch gesellige Veranstaltungen wurden vermehrt angeboten. <br>
Bei der Generalversammlung am 3. Juni 1961 wurde den Mitgliedern eine Planung für den Bau eines Vereinsheimes vorgelegt. Die Mitglieder stimmten zu. Am 22. Juni 1963 wurde als erste Veranstaltung die Generalversammlung im neuen, überwiegend in Eigenleitung erstellten Vereinsheim abgehalten. Der Bau des Vereinsheims war ein Meilenstein für die weitere Entwicklung des Vereins. Die früheren Vereinslokale „Sonne“ und „Lamm“ mussten jetzt nicht mehr in Anspruch genommen werden.<br>
Durch den zunehmenden Spielbetrieb wurden die Platzverhältnisse immer schlechter. Es dauerte aber bis zum 12. Juni 1977, als der lang ersehnte zweite Sportplatz eingeweiht werden konnte. 1987 hatte der „alte“ Platz an der Freudentaler Straße ausgedient. Ein Kunstrasenplatz sorgte fortan für wesentlich bessere Verhältnisse für Training- und Spielbetrieb. Mit der Einweihung des Kleinspielfeldes neben der Sporthalle im Jahr 2002 haben sich die Bedingungen für den Verein nochmals deutlich verbessert Immer wieder wurde von den Vereinsmitgliedern in den Jahren nach dem Bau des Vereinsheims die Gebäudesubstanz verbessert. 1974 fand die erste große Renovierung statt. 1981 wurde das Vereinsheim, die „gute Stube des Vereins“ vergrößert. <br><br>

In den Jahren 1999-2002 fand die nach dem Vereinsheimbau größte Investition statt. Große Teile des Freigeländes wurden überdacht, ein Wirtschaftsgebäude neben dem Vereinsheim erstellt, die sanitären Einrichtungen im Vereinsheim komplett erneuert und vergrößert und neben den Stehwällen Garagen gebaut. <br>
Rüstige Rentner leisteten viele ehrenamtliche Arbeitsstunden unter der Regie des ersten Vorsitzenden Dieter Schuster, der den Verein von 1993 bis 2009 leitete. Mit Kosten von 100.000 Euro war die Maßnahme dennoch ein finanzieller Gewaltakt. <br>
